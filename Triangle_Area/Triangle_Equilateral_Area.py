#Find the triangle equilateral area 

print("Welcome to this program! \n\n Enter varaibles to calculate the Triangle Equilateral Area: \n")

Base = float(input("Enter Base (m): "))
Height = float(input("Enter Height (m): "))

Area = ((Base * Height) / 2)

print("The are of the triangle equilateral is: {:.2f} m^2".format(Area))